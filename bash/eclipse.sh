# download and install eclipse on ubuntu; requires ethernet connection
# to the internet;

# ====================================================================
#
# --------------------------------------------------------------------

LIB=ftp://appslinux01.intellon.com/linux/src/misc
SDK=j2sdk-1_4_2_14-linux-i586.bin

# ====================================================================
#
# --------------------------------------------------------------------

wget ${LIB}/${SDK}
chmod 0755 ./${SDK}
./${SDK}
mv ./j2sdk1.4.2_14 /usr/lib/j2sdk1.4-sun
rm ./${SDK}

# ====================================================================
#
# --------------------------------------------------------------------

apt-get install eclipse eclipse-cdt

# ====================================================================
#
# --------------------------------------------------------------------

echo done.

