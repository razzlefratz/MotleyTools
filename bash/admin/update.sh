#!/bin/bash
# file: /root/update.sh

# ====================================================================
# symbols;
# --------------------------------------------------------------------

. /etc/environment

# ====================================================================
# environment
# --------------------------------------------------------------------

if [ -z "${HOSTS}" ]; then
	echo "symbol HOSTS is undefined."
	exit 1
fi

# ====================================================================
# 
# --------------------------------------------------------------------

for host in ${HOSTS}; do
	echo "---> ${host}"
	scp /etc/environment ${host}:/etc/
	scp /etc/samba/smb.conf ${host}:/etc/samba/
done


